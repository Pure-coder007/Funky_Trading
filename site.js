function setupNavToggle() {
  document.querySelectorAll("[data-nav-toggle]").forEach((button) => {
    const header = button.closest(".site-header");
    if (!header) {
      return;
    }

    button.addEventListener("click", () => {
      const isOpen = header.classList.toggle("nav-open");
      button.setAttribute("aria-expanded", String(isOpen));
      document.body.classList.toggle("nav-open", isOpen);
    });

    header.querySelectorAll(".nav-links a, .nav-panel .cta").forEach((link) => {
      link.addEventListener("click", () => {
        header.classList.remove("nav-open");
        button.setAttribute("aria-expanded", "false");
        document.body.classList.remove("nav-open");
      });
    });
  });
}

function setupSupportBot() {
  document.querySelectorAll("[data-support-bot]").forEach((bot) => {
    const toggle = bot.querySelector("[data-bot-toggle]");
    const close = bot.querySelector("[data-bot-close]");
    const answer = bot.querySelector("[data-bot-answer]");
    let typingTimer;

    toggle.addEventListener("click", () => {
      bot.classList.toggle("is-open");
    });

    close.addEventListener("click", () => {
      bot.classList.remove("is-open");
    });

    bot.querySelectorAll("[data-bot-key]").forEach((button) => {
      button.addEventListener("click", () => {
        const key = button.getAttribute("data-bot-key");
        const message = botAnswers[key] || "Please contact us directly for more details.";
        window.clearTimeout(typingTimer);
        answer.classList.add("is-typing");
        answer.innerHTML = `
          <span class="typing">
            Typing
            <span class="typing-dots" aria-hidden="true">
              <span></span><span></span><span></span>
            </span>
          </span>
        `;

        typingTimer = window.setTimeout(() => {
          answer.classList.remove("is-typing");
          answer.textContent = message;
        }, 1200);
      });
    });
  });
}

