function shuffleItems(items) {
    const copy = [...items];
    for (let i = copy.length - 1; i > 0; i -= 1) {
        const j = Math.floor(Math.random() * (i + 1));
        [copy[i], copy[j]] = [copy[j], copy[i]];
    }
    return copy;
}

function getRandomSubset(items, count) {
    return shuffleItems(items).slice(0, Math.min(count, items.length));
}

function createCarousel(root, items, intervalMs, options = {}) {
    if (!root || !items.length) {
        return;
    }

    let queue = shuffleItems(items);
    let index = 0;

    const media = root.querySelector("[data-carousel-media]");
    const image = root.querySelector("[data-carousel-image]");
    const tag = root.querySelector("[data-carousel-tag]");
    const title = root.querySelector("[data-carousel-title]");
    const text = root.querySelector("[data-carousel-text]");
    const chips = root.querySelector("[data-carousel-chips]");
    const count = root.querySelector("[data-carousel-count]");
    const progress = root.querySelector("[data-carousel-progress]");

    function render(itemIndex) {
        const item = queue[itemIndex];
        if (!item) {
            return;
        }

        if (media) {
            media.style.opacity = "0";
            media.style.transform = "scale(1.06)";
        }

        if (image) {
            image.style.opacity = "0";
            image.style.transform = "scale(1.03)";
        }

        window.setTimeout(() => {
            if (media) {
                media.style.backgroundImage = `url("${item.image}")`;
                media.style.opacity = "1";
                media.style.transform = options.scaleOut || "scale(1.02)";
            }

            if (image) {
                image.style.backgroundImage = `url("${item.image}")`;
                image.style.opacity = "1";
                image.style.transform = "scale(1)";
            }
        }, 120);

        if (tag) tag.textContent = item.tag;
        if (title) title.textContent = item.title;
        if (text) text.textContent = item.text;
        if (count) count.textContent = `${itemIndex + 1} / ${queue.length}`;
        if (progress) progress.style.width = `${((itemIndex + 1) / queue.length) * 100}%`;

        if (chips) {
            chips.innerHTML = "";
            (item.chips || []).forEach((chip) => {
                const span = document.createElement("span");
                span.className = "carousel-chip";
                span.textContent = chip;
                chips.appendChild(span);
            });
        }
    }

    render(index);

    window.setInterval(() => {
        index += 1;
        if (index >= queue.length) {
            queue = shuffleItems(items);
            index = 0;
        }
        render(index);
    }, intervalMs);
}

